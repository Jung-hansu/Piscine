#include "bsq.h"

int	is_printable(char c)
{
	if (32 < c && c <= 126)
		return (1);
	return (0);
}

int	is_dupl(char a, char b, char c)
{
	if (a == b || b == c || c == a)
		return (1);
	return (0);
}
